package com.t1.dao.domain;

public class MianJi {
    private int id;
    private int chang;
    private int kuan;
    private int mianji;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getChang() {
        return chang;
    }

    public void setChang(int chang) {
        this.chang = chang;
    }

    public int getKuan() {
        return kuan;
    }

    public void setKuan(int kuan) {
        this.kuan = kuan;
    }

    public int getMianji() {
        return mianji;
    }

    public void setMianji(int mianji) {
        this.mianji = mianji;
    }
}
